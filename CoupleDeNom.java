
public class CoupleDeNom {
    private Nom nomOriginal;
    private Nom nom;

    public CoupleDeNom() {
    }

    public CoupleDeNom(Nom nomOriginal, Nom nom) {
        this.nomOriginal = nomOriginal;
        this.nom = nom;
    }

    public Nom getNomOriginal() {
        return nomOriginal;
    }

    

    public Nom getNom() {
        return nom;
    }

  
}